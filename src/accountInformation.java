    public void balance(){
        String query = "insert into dbbalanceinformation(Name,AccountNumber,PhoneNumber,Balance)values(?, ?, ?, ?)";
        try{
            pst = con.prepareStatement(query);
            pst.setString(1, jTextFieldName.getText());
            pst.setString(2, jTextFieldAccountNumber.getText());
            pst.setString(3, jTextFieldMobileNumber.getText());
            pst.setString(4, jTextFieldCreditAmount.getText());
            pst.execute();
        }
        
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    
