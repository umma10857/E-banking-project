    private void jButtonCreateAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateAccountActionPerformed
        // TODO add your handling code here:
        String query = "insert into dbaccountinformation(AccountNumber,AccountID,Password,AccountType,SecurityQuestion,Answer,Name,DateofBirth,Gender,Religon,MobileNumber,Address,NIDNumber,BCNumber,CreditAmount)values(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
        try{
            pst = con.prepareStatement(query);
             pst.setString(1, jTextFieldAccountNumber.getText());
             pst.setString(2, jTextFieldAccountID.getText());
             pst.setString(3, jTextFieldPassword.getText());
             pst.setString(4, (String)jComboBoxAccountType.getSelectedItem());
             pst.setString(5, jTextFieldQuestion.getText());
             pst.setString(6, jTextFieldanswer.getText());
             pst.setString(7, jTextFieldName.getText());
             pst.setString(8, ((JTextField)jDateChooserBirthDate.getDateEditor().getUiComponent()).getText());
             jRadioButtonMale.setActionCommand("Male");
             jRadioButtonFemale.setActionCommand("Female");
             jRadioButtonOther.setActionCommand("Other");
             pst.setString(9, buttonGroup1.getSelection().getActionCommand());
             pst.setString(10, (String)jComboBoxReligon.getSelectedItem());
             pst.setString(11, jTextFieldMobileNumber.getText());
             pst.setString(12, jTextFieldAddress.getText());
             pst.setString(13, jTextFieldNIDNumber.getText());
             pst.setString(14, jTextFieldBCNumber.getText());
             pst.setString(15, jTextFieldCreditAmount.getText());
             pst.execute();
             JOptionPane.showMessageDialog(null, "Account has been created!");
             balance();
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButtonCreateAccountActionPerformed

